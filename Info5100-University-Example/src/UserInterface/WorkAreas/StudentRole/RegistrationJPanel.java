    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String courseId = txtCourseID.getText().trim().toLowerCase();
        String courseName = txtCourseName.getText().trim().toLowerCase();
        String teacher = txtTeacher.getText().trim().toLowerCase();

        for (int i = 0; i < availableTableModel.getRowCount(); i++) {
            boolean visible = true;
            if (!courseId.isEmpty() && !availableTableModel.getValueAt(i, 0).toString().toLowerCase().contains(courseId))
                visible = false;
            if (!courseName.isEmpty() && !availableTableModel.getValueAt(i, 1).toString().toLowerCase().contains(courseName))
                visible = false;
            if (!teacher.isEmpty() && !availableTableModel.getValueAt(i, 2).toString().toLowerCase().contains(teacher))
                visible = false;

            if (!visible) {
                availableTableModel.removeRow(i);
                i--;
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnEnrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnrollActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAvailableCourses.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a course to enroll.");
            return;
        }

        String courseId = (String) availableTableModel.getValueAt(selectedRow, 0);
        String courseName = (String) availableTableModel.getValueAt(selectedRow, 1);
        String teacher = (String) availableTableModel.getValueAt(selectedRow, 2);
        int credits = (int) availableTableModel.getValueAt(selectedRow, 3);

        int totalCredits = 0;
        for (int i = 0; i < enrolledTableModel.getRowCount(); i++) {
            totalCredits += (int) enrolledTableModel.getValueAt(i, 3);
        }
        if (totalCredits + credits > 8) {
            JOptionPane.showMessageDialog(this, "Cannot exceed 8 credits per semester.");
            return;
        }

        enrolledTableModel.addRow(new Object[]{courseId, courseName, teacher, credits});
        availableTableModel.removeRow(selectedRow);
    }//GEN-LAST:event_btnEnrollActionPerformed

    private void btnDropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDropActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblEnrolledCourses.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a course to drop.");
            return;
        }

        String courseId = (String) enrolledTableModel.getValueAt(selectedRow, 0);
        String courseName = (String) enrolledTableModel.getValueAt(selectedRow, 1);
        String teacher = (String) enrolledTableModel.getValueAt(selectedRow, 2);
        int credits = (int) enrolledTableModel.getValueAt(selectedRow, 3);

        availableTableModel.addRow(new Object[]{courseId, courseName, teacher, credits});
        enrolledTableModel.removeRow(selectedRow);
    }//GEN-LAST:event_btnDropActionPerformed

