    private void populateAvailableCourses() {
        availableTableModel.setRowCount(0);
        availableTableModel.addRow(new Object[]{"INFO 5100", "Application Engineering", "Kal Bugrara", 4});
        availableTableModel.addRow(new Object[]{"INFO 6150", "Web Tools", "Jane Doe", 4});
        availableTableModel.addRow(new Object[]{"INFO 6205", "Program Structures", "John Smith", 4});
    }
    
    private void populateEnrolledCourses() {
        enrolledTableModel.setRowCount(0);
        if (studentProfile.getTranscript() != null && studentProfile.getTranscript().getCourseList() != null) {
            for (var sa : studentProfile.getTranscript().getCourseList()) {
                var offer = sa.getCourseOffer();
                var course = offer.getSubjectCourse();
                enrolledTableModel.addRow(new Object[]{
                        offer.getCourseNumber(),
                        course.getName(),
                        offer.getFacultyProfile() != null ? offer.getFacultyProfile().getPerson().getPersonId() : "TBD",
                        offer.getCreditHours()
                });
            }
        }
    }
    
    private void updateSelectedCredits() {
        int totalCredits = 0;
        for (int i = 0; i < enrolledTableModel.getRowCount(); i++) {
            totalCredits += (int) enrolledTableModel.getValueAt(i, 3);
        }
        txtSelectedCredits.setText(String.valueOf(totalCredits));
}

