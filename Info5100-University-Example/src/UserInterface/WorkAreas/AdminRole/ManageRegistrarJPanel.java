    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRegistrar.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "You sure you want to delete this registrar?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Person selectedRegistrar = (Person) tblRegistrar.getValueAt(selectedRow, 0);
                RegistrarProfile selectedRegistrarProfile = department.getRegistrarDirectory().findByEmail(selectedRegistrar.getEmail());
                department.getRegistrarDirectory().deleteRegistrar(selectedRegistrarProfile);
                JOptionPane.showMessageDialog(null, "Registrar successfully deleted!", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a registrar from the list", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        refreshTable();

    }//GEN-LAST:event_btnDeleteActionPerformed

