    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRegistrar.getSelectedRow();
        if (selectedRow >= 0) {
            Person selectedRegistrar = (Person) tblRegistrar.getValueAt(selectedRow, 0);
            RegistrarProfile selectedRegistrarProfile = department.getRegistrarDirectory().findByEmail(selectedRegistrar.getEmail());
            ViewRegistrarJPanel vr = new ViewRegistrarJPanel(CardSequencePanel, college, department, selectedRegistrarProfile);

            CardSequencePanel.add("ViewRegistrarJPanel", vr);
            ((java.awt.CardLayout) CardSequencePanel.getLayout()).show(CardSequencePanel, "ViewRegistrarJPanel");
            CardSequencePanel.revalidate();
            CardSequencePanel.repaint();
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a registrar from the list", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnViewActionPerformed

