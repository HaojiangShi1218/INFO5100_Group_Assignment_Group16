    public void refreshTable() {

//clear supplier table
        int rc = tblRegistrar.getRowCount();
        int i;
        for (i = rc - 1; i >= 0; i--) {
            ((DefaultTableModel) tblRegistrar.getModel()).removeRow(i);
        }



        RegistrarDirectory rd = department.getRegistrarDirectory();

       

        for (RegistrarProfile rp : rd.getRegistrarList()) {
            Person registrar = rp.getPerson();
            Object[] row = new Object[7];
            row[0] = registrar;
            row[1] = registrar.getName(); //complete this..
            row[2] = rp.getDepartment();
            row[3] = registrar.getEmail();
            row[4] = registrar.getPhone();
            row[5] = registrar.getAddress();
            row[6] = rp.getOfficeHours();

            ((DefaultTableModel) tblRegistrar.getModel()).addRow(row);
        }

    }

    
