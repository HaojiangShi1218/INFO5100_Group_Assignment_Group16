    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPeople.getSelectedRow();
        if (selectedRow >= 0) {
            Person selectedPerson = (Person) tblPeople.getValueAt(selectedRow, 0);
            PersonRegistrationJPanel pr = new PersonRegistrationJPanel(department, selectedPerson, CardSequencePanel);

            CardSequencePanel.add("PersonRegistrationJPanel", pr);
            ((java.awt.CardLayout) CardSequencePanel.getLayout()).show(CardSequencePanel, "PersonRegistrationJPanel");
            CardSequencePanel.revalidate();
            CardSequencePanel.repaint();
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a person from the list", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnViewActionPerformed

