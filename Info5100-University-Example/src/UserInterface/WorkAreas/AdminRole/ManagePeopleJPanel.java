    public void refreshTable() {

//clear supplier table
        int rc = tblPeople.getRowCount();
        int i;
        for (i = rc - 1; i >= 0; i--) {
            ((DefaultTableModel) tblPeople.getModel()).removeRow(i);
        }



        PersonDirectory pd = department.getPersonDirectory();

       

        for (Person pp : pd.getPersonlist()) {
            Object[] row = new Object[7];
            row[0] = pp;
            row[1] = pp.getName();
            row[2] = pp.getEmail();
            row[3] = pp.getPhone();
            row[4] = pp.getAddress();
            row[5] = pp.getProfile() == null ? "No Profile" : pp.getProfile().getRole();
            row[6] = pp.getAccount() == null ? "No Account" : pp.getAccount().getUserLoginName();

            ((DefaultTableModel) tblPeople.getModel()).addRow(row);
        }

    }

    
