    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String username = fieldUsername.getText();
        String password = fieldPassword.getText();
        String id = fieldID.getText();
        String name = fieldName.getText();
        String email = fieldEmail.getText();
        String phone = fieldPhone.getText();
        
        
        if (username.isBlank() || password.isBlank() || id.isBlank() || name.isBlank() 
                || email.isBlank() || phone.isBlank()) {
            JOptionPane.showMessageDialog(null, "Please fill out all fields");
            return;
        }
        
        
        
        admin.setUsername(username);
        admin.setPassword(password);
        adminProfile.getPerson().setName(name);
        adminProfile.getPerson().setEmail(email);
        adminProfile.getPerson().setPhone(phone);
        
        
        JOptionPane.showMessageDialog(this, "Your information has been updated!", "Success", JOptionPane.INFORMATION_MESSAGE);
        
        fieldUsername.setText("");
        fieldPassword.setText("");
        fieldID.setText("");
        fieldName.setText("");
        fieldEmail.setText("");
        fieldPhone.setText("");
        
        setViewMode();
    }//GEN-LAST:event_btnSaveActionPerformed

