    private void populateTextFields() {
        fieldUsername.setText(admin.getUserLoginName());
        fieldPassword.setText(admin.getPassword());
        fieldID.setText(adminProfile.getPerson().getPersonId());
        fieldName.setText(adminProfile.getPerson().getName());
        fieldEmail.setText(adminProfile.getPerson().getEmail());
        fieldPhone.setText(adminProfile.getPerson().getPhone());
        
    }
    
    
    private void setViewMode() {
        fieldUsername.setEnabled(false);
        fieldPassword.setEnabled(false);
        fieldID.setEnabled(false);
        fieldName.setEnabled(false);
        fieldEmail.setEnabled(false);
        fieldPhone.setEnabled(false);
        //fieldDepartment.setEnabled(false);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }
    
    private void setEditMode() {
        fieldUsername.setEnabled(true);
        fieldPassword.setEnabled(true);
        fieldID.setEnabled(false);
        fieldName.setEnabled(true);
        fieldEmail.setEnabled(true);
        fieldPhone.setEnabled(true);
        btnSave.setEnabled(true);
        btnUpdate.setEnabled(false);
    }
    

