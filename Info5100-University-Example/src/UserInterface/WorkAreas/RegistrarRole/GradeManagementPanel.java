    /** 重载学生表 */
private void reloadStudentTable(java.util.List<Object[]> rows){
    DefaultTableModel m = (DefaultTableModel) tblStudents.getModel();
    m.setRowCount(0);
    for (Object[] r : rows) m.addRow(r);
}

/** 当前选中学生ID */
private String getSelectedStudentId() {
    int r = tblStudents.getSelectedRow();
    if (r < 0) return null;
    Object v = tblStudents.getValueAt(r, 0);
    return v == null ? null : v.toString();
}

/** 当前选中课程ID（右上表） */
private String getSelectedCourseId() {
    int r = tblEnrolledCourses.getSelectedRow();
    if (r < 0) return null;
    Object v = tblEnrolledCourses.getValueAt(r, 0);
    return v == null ? null : v.toString();
}

/** 载入某学生已选课程（右上表） */
private void reloadCoursesForStudent(String sid){
    DefaultTableModel m = (DefaultTableModel) tblEnrolledCourses.getModel();
    m.setRowCount(0);
    if (sid == null) return;
    java.util.Set<String> cids = ds.getCoursesOfStudent(sid);
    for (String cid : cids){
        RegistrarDataStore.Offering o = ds.getOfferingById(cid);
        if (o != null){
            m.addRow(new Object[]{o.courseId, o.title, o.term});
        }
    }
}

/** 载入某学生成绩（右下表） */
private void reloadGradesForStudent(String sid){
    DefaultTableModel m = (DefaultTableModel) tblGrades.getModel();
    m.setRowCount(0);
    if (sid == null) return;
    java.util.Map<String,String> g = ds.getGradesOfStudent(sid);
    for (java.util.Map.Entry<String,String> e : g.entrySet()){
        m.addRow(new Object[]{e.getKey(), e.getValue()});
    }
}

