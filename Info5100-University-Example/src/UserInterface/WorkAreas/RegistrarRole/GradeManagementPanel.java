    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String q = txtStudentSearch.getText();
    if (q == null || q.trim().isEmpty()){
        reloadStudentTable(allStudents);
        return;
    }
    String needle = q.trim().toLowerCase();
    java.util.List<Object[]> out = new java.util.ArrayList<Object[]>();
    for (Object[] r : allStudents){
        String id = String.valueOf(r[0]).toLowerCase();
        String name = String.valueOf(r[1]).toLowerCase();
        String prog = String.valueOf(r[2]).toLowerCase();
        if (id.contains(needle) || name.contains(needle) || prog.contains(needle)) out.add(r);
    }
    reloadStudentTable(out);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String sid = currentSid != null ? currentSid : getSelectedStudentId();
    if (sid == null){ warn("Please select a student."); return; }
    String cid = getSelectedCourseId();
    if (cid == null){ warn("Please select a course on the right-top table."); return; }
    Object sel = cmbLetter.getSelectedItem();
    if (sel == null){ warn("Please choose a letter grade."); return; }
    String letter = sel.toString();

    String r = ds.recordGrade(sid, cid, letter);
    if (!"OK".equals(r)){ warn(r); return; }

    reloadGradesForStudent(sid);
    info("Saved grade " + letter + " for " + sid + " / " + cid + ".");
    }//GEN-LAST:event_btnSaveActionPerformed

